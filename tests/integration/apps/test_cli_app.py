"""
End-to-end integration test for CliApp with DemoBackend — sales domain only.

Tests the full pipeline: domain config loading, testset loading, specification
finding, test execution, and report generation.

Test design leverages deliberate data quality issues in prepare_demo_data.py:
  - sales: core_customer_transactions filters out africa region,
    causing a compare mismatch (NOK).

Demo artifacts (specs, configs, testsets) are generated by
prepare_demo_artifacts.py and read from disk via LocalUserStorage.

The payments domain is tested separately in test_http_app.py.

DO NOT USE PAYMENTS DOMAIN HERE TO NOT MIX DATA BETWEEN FIXTURES!!!
"""

from src.apps.cli.app import CliApp
from src.apps.cli.di import CliDependencyInjector
from src.config import Config
from src.dtos import (
    ReportArtifact,
    ReportArtifactFormat,
    Result,
    Status,
    TestType,
)

from tests.conftest import DemoData


class TestCliAppE2E:
    def test_sales_domain_execution(self, demo_data: DemoData):
        config = Config(
            DATATESTER_DATA_PLATFORM="DEMO",
            DATATESTER_USER_STORAGE_ENGINE="LOCAL",
            DATATESTER_INTERNAL_STORAGE_LOCATION=demo_data.internal_location,
            DATATESTER_DEMO_RAW_PATH=demo_data.raw_path,
            DATATESTER_DEMO_DB_PATH=demo_data.db_path,
        )

        # --- Execute sales domain ---
        app = CliApp(config)
        sales_tr = app.run_tests("sales", "Sales Validation")

        # --- Verify using fresh DI ---
        verify_di = CliDependencyInjector(config)

        # 1. Testruns persisted (20 pre-created demo runs + 1 new)
        tr_driver = verify_di.testrun_driver()
        sales_trs = tr_driver.list_testruns(domain="sales")
        assert len(sales_trs) == 21

        # 2. Sales testrun: 6 testcases, overall NOK
        #    NOK because core_customer_transactions COMPARE fails (africa)
        assert len(sales_tr.results) == 6
        assert sales_tr.result == Result.NOK
        assert sales_tr.status == Status.FINISHED

        sr = {(tc.testobject.name, tc.testtype): tc for tc in sales_tr.results}
        assert sr[("stage_customers", TestType.SCHEMA)].result == Result.OK
        assert sr[("stage_transactions", TestType.SCHEMA)].result == Result.OK
        assert sr[("core_customer_transactions", TestType.ROWCOUNT)].result == Result.OK
        assert sr[("core_customer_transactions", TestType.COMPARE)].result == Result.NOK
        assert sr[("stage_customers", TestType.STAGECOUNT)].result == Result.OK
        assert sr[("stage_transactions", TestType.STAGECOUNT)].result == Result.OK
        for tc in sales_tr.results:
            assert tc.status == Status.FINISHED

        # 3. Testcases are persisted and loadable by id
        for tc in sales_tr.results:
            loaded = tr_driver.load_testcase(testcase_id=tc.id)
            assert str(loaded.id) == str(tc.id)
            assert loaded.result == tc.result
            assert loaded.testtype == tc.testtype

        # 4. Report artifacts can be created for all testcases
        report_driver = verify_di.report_driver()
        for tc in sales_tr.results:
            txt_artifact = report_driver.create_testcase_report_artifact(
                testcase_id=tc.id,
                artifact=ReportArtifact.REPORT,
                artifact_format=ReportArtifactFormat.TXT,
            )
            assert isinstance(txt_artifact, bytes)
            assert len(txt_artifact) > 0

        # 5. Testrun XLSX report artifact can be created
        xlsx_artifact = report_driver.create_testrun_report_artifact(
            testrun_id=sales_tr.id,
            artifact_format=ReportArtifactFormat.XLSX,
        )
        assert isinstance(xlsx_artifact, bytes)
        assert xlsx_artifact.startswith(b"PK\x03\x04")
